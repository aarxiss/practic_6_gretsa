# Запитуємо суму поповнення рахунку
amount = float(input("Введіть суму поповнення рахунку (грн): "))

# Перевіряємо, чи сума менша за 10 грн
if amount < 10:
    print("Мінімальна сума поповнення – 10 грн!")
# Перевіряємо, чи сума перевищує 3000 грн
elif amount > 3000:
    print("Сума поповнення занадто велика!")
# Якщо сума в межах допустимого діапазону
else:
    print(f"Поповнення на суму {amount} грн виконано успішно!")
