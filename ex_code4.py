# Запитуємо температуру приміщення в градусах Цельсія
temperature = float(input("Введіть температуру приміщення (°C): "))

# Перевіряємо, чи температура менше 16 градусів
if temperature < 16:
    print("Температура занизька! Увімкніть обігрівач.")
# Перевіряємо, чи температура більше 28 градусів
elif temperature > 28:
    print("Температура зависока! Увімкніть кондиціонер.")
# Якщо температура в межах норми (16–28 градусів)
else:
    print("Температура комфортна.")
