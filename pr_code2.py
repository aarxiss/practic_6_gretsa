# Кількість товару на складі
stock_quantity = 20

# Запитуємо у користувача кількість товару, яку він хоче замовити
order_quantity = int(input("Введіть кількість товару для замовлення: "))

# Перевірка введеної кількості
if order_quantity <= 0:
    print("Помилка: некоректна кількість!")
elif order_quantity > stock_quantity:
    print("На складі недостатньо товару!")
else:
    print("Ваше замовлення прийнято!")
