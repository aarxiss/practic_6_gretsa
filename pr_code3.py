# Запитуємо у користувача пароль
password = input("Введіть пароль для входу: ")

# Перевірка на короткий пароль
if len(password) < 8:
    print("Пароль надто короткий!")
# Перевірка на занадто простий пароль
elif password == "password" or password == "12345678":
    print("Пароль занадто простий!")
# Якщо пароль задовольняє вимоги
else:
    print("Вхід дозволено!")
